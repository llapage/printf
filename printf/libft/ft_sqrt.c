int		ft_sqrt(int nb)
{
	int	i;
	int	x;
	int	count;

	i = 1;
	x = 0;
	count = 0;
	while (x < nb)
	{
		x = x + i;
		i = i + 2;
		count++;
	}
	if (x != nb)
		return (0);
	return (count);
}
